/**
 * <h1>Calculadora</h1>
 * A calculadora realiza operações matemáticas entre numeros inteiros
 * @author Giovana
 * @version 1.0
 * @since 03/09/2024
 */
public class Calculadora {
	/**
	 * Este método é utilizado para somar dois números 
	 * @param numeroUm este é o primeiro parâmetro do método
	 * @param numeroDois este é o segundo parâmetro do método 
	 * @return it o resultado deste método é a soma dos dois números
	 */
	
	public int somar(int numeroUm, int numeroDois) {
		return numeroUm + numeroDois;
	}
	
	/**
	 * Este método é utilizado para subtrair dois números 
	 * @param numeroUm este é o primeiro parâmetro do método
	 * @param numeroDois este é o segundo parâmetro do método 
	 * @return int o resultado deste método é a subtração dos dois números
	 */
	
	public int subtrair(int numeroUm, int numeroDois) {
		return numeroUm - numeroDois;
 }
}



